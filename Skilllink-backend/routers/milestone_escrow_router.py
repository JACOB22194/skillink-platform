# ── POST /milestones/{id}/admin/resolve ───────────────────────────────────────

@router.post(
    "/milestones/{milestone_id}/admin/resolve",
    response_model=schema.MilestoneDetailResponse,
    summary="Admin dispute arbitration (admin only)",
    description="resolution: force_pay | force_refund | split (requires split_percentage 1–99)",
)
def admin_resolve_milestone(
    milestone_id:    int,
    body:            schema.AdminResolveRequest,
    me:              models.User = Depends(require_admin),
    db:              Session     = Depends(get_db),
    idempotency_key: str        = Header(..., description="Unique UUID — prevents duplicate arbitration"),
):
    if _check_idempotency(idempotency_key, db):
        return _get_milestone_or_404(milestone_id, db)

    return state_machine.resolve_arbitration(
        milestone_id=milestone_id,
        resolution_type=body.resolution,
        split_percentage=body.split_percentage,
        actor=me,
        db=db,
        note=body.note,
        idempotency_key=idempotency_key,
    )


# ── POST /contracts/{id}/auto-split ───────────────────────────────────────────

@router.post(
    "/contracts/{contract_id}/auto-split",
    response_model=schema.MilestoneAutoSplitResponse,
    status_code=201,
    summary="Auto-split contract budget into milestones (client)",
    description="Creates new milestones in AWAITING_FUNDS status. Contract must be active with no existing new-style milestones.",
)
def auto_split_milestones(
    contract_id: int,
    me:          models.User = Depends(get_current_user),
    db:          Session     = Depends(get_db),
):
    contract = (
        db.query(models.Contract)
        .options(
            joinedload(models.Contract.project),
            joinedload(models.Contract.milestones),
        )
        .filter(models.Contract.contract_id == contract_id)
        .first()
    )
    if not contract:
        raise HTTPException(404, "Contract not found.")

    if contract.status != models.ContractStatus.active:
        raise HTTPException(400, f"Contract is not active (status: {contract.status.value}).")

    # Access check
    if me.role != models.UserRole.admin:
        client = db.query(models.Client).filter(models.Client.user_id == me.id).first()
        if not client or contract.project.client_id != client.client_id:
            raise HTTPException(403, "Only the client or admin can auto-split milestones.")

    # Guard: no existing new-style milestones
    existing_new = [m for m in contract.milestones if m.status in NEW_STYLE_STATUSES]
    if existing_new:
        raise HTTPException(
            400,
            "Contract already has new-style escrow milestones. Cannot auto-split again.",
        )

    budget = contract.project.budget
    if not budget or budget <= 0:
        raise HTTPException(400, "Project budget must be set before auto-splitting.")

    splits = split_milestones(budget)

    # Space milestones 2 weeks apart
    now = datetime.now(timezone.utc)
    created = []
    for i, s in enumerate(splits):
        deadline = now + timedelta(weeks=(i + 1) * 2)
        m = models.Milestone(
            contract_id=contract_id,
            title=s["title"],
            description=None,
            amount=s["amount"],
            status=models.MilestoneStatus.awaiting_funds,
            due_date=deadline,
            deadline=deadline,
        )
        db.add(m)
        created.append(s)

    db.commit()

    return schema.MilestoneAutoSplitResponse(
        milestones=created,
        total_amount=budget,
        count=len(created),
    )
